        <script>
        $(function () {

            /* ---- REVEAL on scroll ---- */
            function checkReveal() {
                $('.reveal').each(function () {
                    if ($(this).offset().top < $(window).scrollTop() + $(window).height() - 80) {
                        $(this).addClass('revealed');
                    }
                });
            }
            $(window).on('scroll', checkReveal);
            checkReveal();

            /* ---- SCROLL suave para #portfolio ---- */
            $('.iv-hero-scroll-btn').on('click', function (e) {
                e.preventDefault();
                $('html, body').animate({ scrollTop: $('#portfolio').offset().top - 80 }, 700, 'swing');
            });

            /* ---- FILTROS do portfólio ---- */
            $('.iv-filter-btn').on('click', function () {
                var filter = $(this).data('filter');
                $('.iv-filter-btn').removeClass('iv-filter-active');
                $(this).addClass('iv-filter-active');

                if (filter === 'all') {
                    $('.iv-portfolio-item').each(function (i) {
                        var $el = $(this);
                        setTimeout(function () {
                            $el.removeClass('iv-item-hidden').addClass('iv-item-show');
                        }, i * 50);
                    });
                } else {
                    $('.iv-portfolio-item').each(function (i) {
                        var $el = $(this);
                        if ($el.data('category') === filter) {
                            setTimeout(function () {
                                $el.removeClass('iv-item-hidden').addClass('iv-item-show');
                            }, i * 50);
                        } else {
                            $el.removeClass('iv-item-show').addClass('iv-item-hidden');
                        }
                    });
                }
            });

            /* ---- LIGHTBOX ---- */
            var images = [];
            var currentIndex = 0;

            // Coleta imagens visíveis
            function buildImageList() {
                images = [];
                $('.iv-portfolio-item:not(.iv-item-hidden) .iv-portfolio-card').each(function () {
                    images.push({
                        src: $(this).find('.iv-portfolio-img').attr('src'),
                        alt: $(this).find('.iv-portfolio-img').attr('alt'),
                        name: $(this).find('.iv-portfolio-name').text(),
                        cat: $(this).find('.iv-portfolio-cat').text()
                    });
                });
            }

            function openLightbox(index) {
                buildImageList();
                currentIndex = index;
                showImage(currentIndex);
                $('#ivLightbox').addClass('iv-lightbox-open');
                $('body').addClass('iv-no-scroll');
            }

            function closeLightbox() {
                $('#ivLightbox').removeClass('iv-lightbox-open');
                $('body').removeClass('iv-no-scroll');
            }

            function showImage(index) {
                if (images.length === 0) return;
                if (index < 0) index = images.length - 1;
                if (index >= images.length) index = 0;
                currentIndex = index;
                var img = images[index];
                $('#ivLightboxImg').attr('src', img.src).attr('alt', img.alt);
                $('#ivLightboxCaption').html('<strong>' + img.name + '</strong><span>' + img.cat + '</span>');
                $('#ivLightboxCounter').text((index + 1) + ' / ' + images.length);
            }

            // Abrir pelo botão de zoom (usa o índice da lista completa original)
            $(document).on('click', '.iv-portfolio-zoom', function () {
                var $card = $(this).closest('.iv-portfolio-card');
                buildImageList();
                var src = $card.find('.iv-portfolio-img').attr('src');
                var foundIndex = 0;
                $.each(images, function (i, img) {
                    if (img.src === src) { foundIndex = i; return false; }
                });
                openLightbox(foundIndex);
            });

            // Abrir clicando na imagem
            $(document).on('click', '.iv-portfolio-img', function () {
                $(this).closest('.iv-portfolio-card').find('.iv-portfolio-zoom').trigger('click');
            });

            $('#ivLightboxClose, #ivLightboxBackdrop').on('click', closeLightbox);
            $('#ivLightboxNext').on('click', function () { showImage(currentIndex + 1); });
            $('#ivLightboxPrev').on('click', function () { showImage(currentIndex - 1); });

            // Teclado
            $(document).on('keydown', function (e) {
                if (!$('#ivLightbox').hasClass('iv-lightbox-open')) return;
                if (e.key === 'Escape') closeLightbox();
                if (e.key === 'ArrowRight') showImage(currentIndex + 1);
                if (e.key === 'ArrowLeft') showImage(currentIndex - 1);
            });

            // Swipe mobile
            var touchStartX = 0;
            document.getElementById('ivLightbox').addEventListener('touchstart', function (e) {
                touchStartX = e.changedTouches[0].screenX;
            }, { passive: true });
            document.getElementById('ivLightbox').addEventListener('touchend', function (e) {
                var diff = touchStartX - e.changedTouches[0].screenX;
                if (Math.abs(diff) > 50) {
                    if (diff > 0) showImage(currentIndex + 1);
                    else showImage(currentIndex - 1);
                }
            }, { passive: true });

        });
        </script>

